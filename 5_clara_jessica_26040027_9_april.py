# ========================================
# SECTION 2 ISSUES
# ========================================

issues = []

for i in range(5):

    while True:
        issue_id = input("id: ")
        if issue_id == "":
            print("invalid id")
            continue
        if issue_id not in [x["id"] for x in issues]:
            break
        else:
            print("duplicate id not allowed")

    while True:
        title = input("title: ")
        if title != "":
            break

    while True:
        issue_type = input("type (Bug/Feature): ").lower()
        if issue_type in ["bug", "feature"]:
            break
        else:
            print("invalid type")

    while True:
        priority = input("priority (Critical/High/Medium/Low): ").capitalize()
        if priority in ["Critical", "High", "Medium", "Low"]:
            break
        else:
            print("invalid priority")

    while True:
        reporter = input("reporter: ")
        if reporter.replace(" ", "").isalpha():
            break

    while True:
        status = input("status (Open/In Progress/Resolved): ").title()
        if status in ["Open", "In Progress", "Resolved"]:
            break
        else:
            print("invalid status")

    issue = {
        "id": issue_id,
        "title": title,
        "type": issue_type,
        "priority": priority,
        "reporter": reporter,
        "status": status
    }

    issues.append(issue)

open_count = 0
for i in issues:
    if i["status"] == "Open":
        open_count += 1

print("Open issues:", open_count)

issues[0]["priority"] = "Critical"

print("Last two issues:", issues[-2:])


