priority_count = {}

for i in issues:
    p = i["priority"]
    if p in priority_count:
        priority_count[p] += 1
    else:
        priority_count[p] = 1

print("Keys:", priority_count.keys())
print("Values:", priority_count.values())

for k, v in priority_count.items():
    print(k, v)

status_groups = {}

for i in issues:
    s = i["status"]
    if s not in status_groups:
        status_groups[s] = []
    status_groups[s].append(i["title"])

print("Status groups:", status_groups)

report_count = {}

for i in issues:
    r = i["reporter"]
    if r in report_count:
        report_count[r] += 1
    else:
        report_count[r] = 1

top = ""
max_count = 0

for k in report_count:
    if report_count[k] > max_count:
        max_count = report_count[k]
        top = k

print("Top reporter:", top, max_count)

