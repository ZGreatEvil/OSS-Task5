contributors = []

for i in range(4):

    while True:
        name = input("name: ")
        if name.replace(" ", "").isalpha():
            break
        else:
            print("invalid name")

    while True:
        role = input("role: ")
        if role != "":
            break

    while True:
        lang = input("language: ")
        if lang.replace(" ", "").isalpha():
            break

    while True:
        commits = input("commits: ")
        if commits.isdigit():
            commits = int(commits)
            break
        else:
            print("invalid number")

    while True:
        country = input("country: ")
        if country.replace(" ", "").isalpha():
            break

    contributor = {
        "name": name,
        "role": role,
        "language": lang,
        "commits": commits,
        "country": country
    }

    contributors.append(contributor)

names = []
for c in contributors:
    names.append(c["name"])

names.sort()

print("Sorted names:", names)
print("Last name:", names[-1])
print("First two:", names[:2])

